/** Returns the flag emoji for a country name, or "" if not found. */
export function flagForCountry(name) {
  for (const countries of Object.values(REGIONS)) {
    const found = countries.find((c) => c.name === name);
    if (found) return found.flag;
  }
  return "";
}
